public class Variable {
    public static void main(String[] args) {

        // Declares three int variables: number1, number2, and number3.
        int number1, number2, number3;

        // Example of variable initialization.
        int number4 = 10, number5 = 20;

        // initializes a byte type variable.
        byte someByte = 22;

        // declares and assigns a value of PI.
        double pi = 3.14159;

        // the char variable oneChar is initialized with value 'a'
        char oneChar = 'a';

        System.out.println("Number 4: " + number4);
        System.out.println("Number 5: " + number5);
        System.out.println("Some Byte: " + someByte);
        System.out.println("Pi: " + pi);
        System.out.println("One Char: " + oneChar);
    }

}