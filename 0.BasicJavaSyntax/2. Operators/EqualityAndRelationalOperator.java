public class EqualityAndRelationalOperator {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;
        System.out.println("value1 is :" + value1);
        System.out.println("value2 is :" + value2);

        if (value1 == value2)
            System.out.println("value1 == value2");
        if (value1 != value2)
            System.out.println("value1 != value2");
        if (value1 > value2)
            System.out.println("value1 > value2");
        if (value1 < value2)
            System.out.println("value1 < value2");
        if (value1 <= value2)
            System.out.println("value1 <= value2");
    }
}
